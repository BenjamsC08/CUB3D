int	ft_isprime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	i = 2;
	while (i <= nb / i)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}

int	ft_next_prime(int nb)
{
	while (ft_isprime(nb) == 0)
		nb++;
	return (nb);
}
